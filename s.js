function labSwi(val) {
    let answer="";
    switch (val) {
        case 1:
            answer='one';
            break;
        case 2:
            answer='two';
            break;
        case 3:
            answer='three';
            break;
        case 4:
            answer='four';
            break;
    }
    return answer;
}

